def mezclarPosibilidades(k):

    setDePosibilidades = {n for n in range(1, k + 1)}
    posibilidades = (list(setDePosibilidades))
    random.shuffle(posibilidades)
    return posibilidades

def get_tamaño(unTablero):

    tamaño = len(unTablero)
    return tamaño


def sudokuSolver(bo, k, posibilidades):
    find = celdaVacia(bo)

    if not find:
        return True
    else:
        row, col = find

    for i in posibilidades:

        if validoEnTablero(bo, i, (row, col), k):
            bo[row][col] = i

            if sudokuSolver(bo, k, posibilidades):
                return True

            bo[row][col] = 0

    return False

def validoEnTablero(bo, num, pos, k):
    raiz_k=int(math.sqrt(k))
    # Check row

    for i in range(len(bo[0])):
        if bo[pos[0]][i] == num and pos[1] != i:
            return False

    # Check column
    for i in range(len(bo)):
        if bo[i][pos[1]] == num and pos[0] != i:
            return False

    # Check box
    box_x = pos[1] // raiz_k
    box_y = pos[0] // raiz_k

    for i in range(box_y*raiz_k, box_y*raiz_k + raiz_k):
        for j in range(box_x * raiz_k, box_x*raiz_k + raiz_k):
            if bo[i][j] == num and (i,j) != pos:
                return False

    return True


